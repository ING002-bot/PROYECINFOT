        <h1>Políticas de Privacidad - Panel Administrativo INFOTEC</h1>

        <p>Esta política de privacidad describe cómo INFOTEC trata y protege los datos personales que administra a través del panel de administrador. Al hacer uso de esta plataforma, aceptas los términos aquí descritos.</p>

        <h2>1. Uso de la información</h2>
        <p>La información personal de los usuarios (nombre, correo, historial de compras, etc.) se utiliza exclusivamente para:</p>
        <ul>
            <li>Gestión de usuarios registrados.</li>
            <li>Procesamiento y seguimiento de ventas.</li>
            <li>Atención de soporte técnico.</li>
            <li>Generación de reportes internos.</li>
        </ul>

        <h2>2. Acceso restringido</h2>
        <p>El acceso a esta información está limitado exclusivamente a los administradores autorizados. Todo acceso no autorizado será investigado y reportado.</p>

        <h2>3. Protección de datos</h2>
        <p>INFOTEC implementa medidas de seguridad técnicas y organizativas para proteger los datos almacenados, incluyendo encriptación de contraseñas y protección contra accesos no autorizados.</p>

        <h2>4. Confidencialidad</h2>
        <p>Los administradores están obligados a mantener estricta confidencialidad sobre toda la información gestionada dentro del sistema.</p>

        <h2>5. Modificaciones</h2>
        <p>INFOTEC se reserva el derecho de modificar estas políticas en cualquier momento. Los cambios se publicarán en esta misma sección y se notificarán a los administradores activos.</p>

        <h2>6. Contacto</h2>
        <p>Si tienes alguna duda sobre esta política, puedes escribirnos a <strong>soporte@infotec.com</strong>.</p>
